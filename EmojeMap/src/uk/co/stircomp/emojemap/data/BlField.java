package uk.co.stircomp.emojemap.data;

public class BlField {
	
	private String name, value;
	
	public BlField(String name, String value) {
		
		this.name = name;
		this.value = value;
		
	}
	
	public String getName() {
		
		return this.name;
		
	}
	
	public String getValue() {
		
		return this.value;
		
	}

}
